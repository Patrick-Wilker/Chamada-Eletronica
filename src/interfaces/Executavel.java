    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        String cpf = txtCpf.getText();
        String senha = String.valueOf(txtSenha.getPassword());
        int pos = 0;
        if (cpf.equals("") || senha.equals("")) {
            JOptionPane.showMessageDialog(null, "Existe campo em branco");
        } else {
            boolean achou = false;
            for (int i = 0; i < Usuario.usuarios.size(); i++) {
                if (cpf.equals(Usuario.usuarios.get(i).getCpf())) {
                    achou = true;
                }
            }
            if (achou == true) {

                if (txtCpf.getText().equals("") == false || txtSenha.getPassword().equals("") == false) {
                    for (int i = 0; i < Usuario.usuarios.size(); i++) {
                        if (cpf.equals(Usuario.usuarios.get(i).getCpf())) {
                            pos = i;
                        }
                    }
                    if (senha.equals(Usuario.usuarios.get(pos).getSenha()) == false) {
                        JOptionPane.showMessageDialog(null, "SENHA INCORRETA");
                    } else {
                        if (cpf.equals(Usuario.usuarios.get(pos).getCpf()) && Usuario.usuarios.get(pos).isPermissao() == true
                                && senha.equals(Usuario.usuarios.get(pos).getSenha())) {
                            MenuAdm TelaPrincipal = new MenuAdm();
                            TelaPrincipal.setVisible(true);
                            txtCpf.setText("");
                            txtSenha.setText("");
                        } else if (cpf.equals(Usuario.usuarios.get(pos).getCpf()) && Usuario.usuarios.get(pos).isPermissao() == false
                                && senha.equals(Usuario.usuarios.get(pos).getSenha())) {
                            MenuAluno menuAluno = new MenuAluno();
                            menuAluno.setVisible(true);
                            txtCpf.setText("");
                            txtSenha.setText("");
                        }
                    }

                }

            } else {
                JOptionPane.showMessageDialog(null, "CPF não cadastrado");
            }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

